#include <unistd.h>

void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int	ri;
	int	t;

	i = 0;
	ri = size - 1;
	while (i < (size / 2))
	{
		t = tab[i];
		tab[i] = tab[ri];
		tab[ri] = t;
		i++;
		ri--;
	}
}
