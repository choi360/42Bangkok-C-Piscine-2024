#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

int 	main(int argc, char *argv[])
{
	int s;

	if (argc > 1)
	{
		s = 1;
		while (s < argc - 1)
		{
			while (*argv[s])
			{
				ft_putchar(*argv[s]);
				*argv[s]++;
			}
			ft_putchar('\n');
			s++;
		}
	}
	return (0);
}


