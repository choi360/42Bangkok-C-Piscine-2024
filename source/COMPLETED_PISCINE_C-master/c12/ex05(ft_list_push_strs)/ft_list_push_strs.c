{
	t_list *current;
	t_list *last;
	int i;

	i = -1;
	current = ((void *)0);
	last = ((void *)0);
	while (++i < size)
	{
		current = ft_create_elem(strs[i]);
		current->next = last;
		last = current;
	}
	return (current);
}


