int		iss_alpha(char c)
{
	if ((c >= 'a') && (c <= 'z'))
		return (1);
	if ((c >= 'A') && (c <= 'Z'))
		return (1);
	if ((c >= '0') && (c <= '9'))
		return (1);
	return (0);
}

int		is_maj(char c)
{
	return ((c >= 'A') && (c <= 'Z'));
}

char	is_min(char c)
{
	return ((c >= 'a') && (c <= 'z'));
}

char	*ft_strcapitalize(char *str)
{
	int	i;
	int	word_new;

	i = 0;
	word_new = 1;
	while (str[i])
	{
		if (is_min(str[i]) && (word_new == 1))
			str[i] = ((str[i] - 'a') + 'A');
		else if (is_maj(str[i]) && (word_new == 0))
			str[i] = ((str[i] - 'A') + 'a');
		if (iss_alpha(str[i]))
			word_new = 0;
		else
			word_new = 1;
		i++;
	}
	return (str);
}



