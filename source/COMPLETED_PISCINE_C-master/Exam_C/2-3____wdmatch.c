#include <unistd.h>

void	ft_putstr(char *str)
{
	while (*str)
		write(1, str++, 1);
}

int		ft_strlen(char *str)
{
	int i = 0;
	while (str[i])
		i++;
	return (i);
}

void	ft_wdmatch(char *s1, char *s2)	/* Принимаем адреса двух строк */
{
	int		i;						/* Обьявляем счетчик i для первой строки */
	int		j;						/* Обьявляем счетчик j для второй строки */
	int		lenght;					/* Обьявляем переменную для хранения длинну строки */

	i = 0;							/* Инициализируем счетчик i нулем */
	j = 0;							/* Инициализируем счетчик j нулем */
	lenght = ft_strlen(s1);
	while (s2[j] && i < lenght)		/* Запускаем цикл работающий до тех пор пока счетчик i 
									** не будет равен размеру первой строки или пока не дойдем 
									** до конца второй строки */
	{
		if (s2[j] == s1[i])			/* Если символ из второй строки такой же как и символ из
									** первой строки то переходим к след символу первой строки
									** и след символу второй строки для дальнейшей проверки
									** и так далее пока не дойдем до конца первой строки */
									/* Если символ из второй строки не такой же как символ
									** из первой стороки то переходим к след ячейке второй строки
									** и так далее пока не найдем такой же символ во второй строке
									** или пока вторая строка не закончится */
			i++;					/* увеличение счетчика для перехода к след ячейке первой строки */
		j++;						/* увеличение счетчика для перехода к след ячейке второй строки */
	}
	if (i == lenght)				/* Если дошли до конца первой строки значит каждый из символов
									** присутствует во второй строке в том же порядке хоть и не последовательно */
		ft_putstr(s1);				/* значит печатаем первую строку */
}

int main(int argc, char **argv)		/* Программа принимает в аргументах строки*/
{
	if (argc == 3)					/* Если в аргументах присутствует имя программы и нужные нам 2 строки */
		ft_wdmatch(argv[1],argv[2]);/* то берем эти две строки и отправляем их адреса в функцию */
	write(1, "\n", 1);				/* Печатаем символ переноса строки */
	return (0);
}



