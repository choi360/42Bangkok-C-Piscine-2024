#include <unistd.h>

void	ft_rot_13(char *str)
{
	int		i;
	char	mvup;
	char	mvdwn;

	i = 0;
	while (str[i])
	{
		mvup = str[i] + 13;
		mvdwn = str[i] - 13;
		if (('A' <= str[i] && 'M' >= str[i]) || ('a' <= str[i] && 'm' >= str[i]))
			write(1, &mvup, 1);
		else if (('N' <= str[i] && 'Z' >= str[i]) || ('n' <= str[i] && 'z' >= str[i]))
			write(1, &mvdwn, 1);
		else
			write(1, &str[i], 1);
		i += 1;
	}
}

int		main(int argc, char *argv[])
{
	if (argc == 2)
	 ft_rot_13(argv[1]);
	ft_putchar('\n');
	return (0);
}
