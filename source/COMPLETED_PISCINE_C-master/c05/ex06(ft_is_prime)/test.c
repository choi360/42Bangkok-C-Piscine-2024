void	ft_putchar(char c)			/* функция вывода символа */
{
	write(1, &c, 1);
}

/* ************************************************************************** */
/* ************************************************************************** */

void	ft_putstr(char *s)			/* функция вывода строки */
{
	while (*s)
		write(1, s++, 1);
}

